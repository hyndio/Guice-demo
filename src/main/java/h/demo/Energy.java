public class Energy {

	private char c;

	public Energy() {
		super();
	}

	public Energy(char c) {
		super();
		this.c = c;
	}

	public char getC() {
		return c;
	}

	public void setC(char c) {
		this.c = c;
	}

	@Override
	public String toString() {
		return "Energy [c=" + c + "]";
	}
	
	

}
